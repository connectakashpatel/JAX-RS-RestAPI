package Model;

public class Person {
	
	private String name;
	private int id;
	
	
	// KP Example
	private double age;

	public double getAge() {
		return age;
	}
	public void setAge(double age) {
		this.age = age;
	}
	
	// *********************************
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
}
